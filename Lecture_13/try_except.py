    while True:
        try:
            num = int(input(text))
            div = 100 / num
            break
        except ValueError as e:
            print(f'–í–∞—à –≤–≤–æ–¥ –ø—Ä–∏–≤—ë–ª –∫ –æ—à–∏–±–∫–µ ValueError: {e}\n' f'–ü–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞')
        except ZeroDivisionError as e:
            div = float('inf')
            break
    return num, div


